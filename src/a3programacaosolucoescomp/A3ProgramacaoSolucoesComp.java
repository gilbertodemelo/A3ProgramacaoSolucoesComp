    private void loginConfirmaButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginConfirmaButtonMouseClicked
        try{
            if(loginEmailTextField.getText().equals(usuario.getEmail()) && loginSenhaTextField.getText().equals(usuario.getSenha())){
                Dialog.setVisible(false);
                usuarioLogadoLabel.setText("Bem-vindo(a) " + usuario.getPrimeiroNome());
                contatoLogadoLabel.setText("Contato disponível: " + usuario.getContato());
                card = (CardLayout)controladorJFramePanel.getLayout();
                card.show(controladorJFramePanel, "logadoPanel");
            }else{
                dadosIncorretosLabel.setVisible(true);
            }
        }catch(Exception ex){
            dadosIncorretosLabel.setVisible(true);
        }
    }//GEN-LAST:event_loginConfirmaButtonMouseClicked

