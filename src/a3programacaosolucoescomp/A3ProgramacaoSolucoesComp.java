    private void voltarInicioButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_voltarInicioButtonMouseClicked
        if(tipoUsuarioCheckBox.isSelected()){
            usuario = new Promoter(nomeTextField.getText(), sobrenomeTextField.getText(), emailTextField.getText(), senhaTextField.getText(), cpfTextField.getText(), rgTextField.getText(), telefoneTextField.getText(), cepTextField.getText(), enderecoTextField.getText(), contatoTextField.getText(), "promoter", empresaTextField.getText(), cnpjTextField.getText());   
            empresaTextField.setEnabled(false);
            empresaTextField.setText("");
            cnpjTextField.setEnabled(false);
            cnpjTextField.setText("");
        }
        card = (CardLayout)controladorJFramePanel.getLayout();
        card.show(controladorJFramePanel, "inicioPanel");
    }//GEN-LAST:event_voltarInicioButtonMouseClicked

    private void confirmarCadastroButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmarCadastroButtonMouseClicked
        if(tipoUsuarioCheckBox.isSelected()){
            usuario = new Promoter(nomeTextField.getText(), sobrenomeTextField.getText(), emailTextField.getText(), senhaTextField.getText(), cpfTextField.getText(), rgTextField.getText(), telefoneTextField.getText(), cepTextField.getText(), enderecoTextField.getText(), contatoTextField.getText(), "promoter", empresaTextField.getText(), cnpjTextField.getText());   
            empresaTextField.setEnabled(false);
            empresaTextField.setText("");
            cnpjTextField.setEnabled(false);
            cnpjTextField.setText("");
        }else{
            usuario = new Usuario(nomeTextField.getText(), sobrenomeTextField.getText(), emailTextField.getText(), senhaTextField.getText(), cpfTextField.getText(), rgTextField.getText(), telefoneTextField.getText(), cepTextField.getText(), enderecoTextField.getText(), contatoTextField.getText(), "participante");   
        }
        nomeTextField.setText("");
        sobrenomeTextField.setText("");
        emailTextField.setText("");
        senhaTextField.setText("");
        cpfTextField.setText("");
        rgTextField.setText("");
        telefoneTextField.setText("");
        cepTextField.setText("");
        enderecoTextField.setText("");
        contatoTextField.setText("");
        usuarioLogadoLabel.setText("Bem-vindo(a) " + usuario.getPrimeiroNome());
        contatoLogadoLabel.setText("Contato disponível: " + usuario.getContato());
        card = (CardLayout)controladorJFramePanel.getLayout();
        card.show(controladorJFramePanel, "logadoPanel");
    }//GEN-LAST:event_confirmarCadastroButtonMouseClicked

    private void tipoUsuarioCheckBoxStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_tipoUsuarioCheckBoxStateChanged
        if(tipoUsuarioCheckBox.isSelected()){
            empresaTextField.setEnabled(true);
            cnpjTextField.setEnabled(true);
        }else{
            empresaTextField.setEnabled(false);
            empresaTextField.setText("");
            cnpjTextField.setEnabled(false);
            cnpjTextField.setText("");
        }
    }//GEN-LAST:event_tipoUsuarioCheckBoxStateChanged

